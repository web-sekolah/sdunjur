);

/*TABLE BARANG_POST*/
CREATE TABLE BARANG_POST(
id_barang VARCHAR(10)PRIMARY KEY,
kategori VARCHAR(10) NOT NULL,
kuota INT NOT NULL,
nama_barang VARCHAR(20) NOT NULL,
harga INT NOT NULL,
foto TEXT,
waktu_posting TIMESTAMP NOT NULL,
no_ktp VARCHAR(16),
CONSTRAINT tt FOREIGN KEY (no_ktp, waktu_posting) REFERENCES POST (no_ktp, waktu_posting)
);

/*TABLE LIST_BARANG*/
CREATE TABLE LIST_BARANG(
kualitas INT NOT NULL,
id_barang VARCHAR(10),
no_invoice VARCHAR(20),
CONSTRAINT nn PRIMARY KEY(id_barang,no_invoice),
CONSTRAINT mm FOREIGN KEY (id_barang) REFERENCES BARANG_POST (id_barang),
CONSTRAINT uu FOREIGN KEY (no_invoice) REFERENCES INVOICE (no_invoice)
);

/*TABLE TRANSAKSI*/
